add_action('init', 'use_jquery_from_google');

function use_jquery_from_google () {
	if (is_admin()) {
		return;
	}

	global $wp_scripts;
	if (isset($wp_scripts->registered['jquery']->ver)) {
		$ver = $wp_scripts->registered['jquery']->ver;
                $ver = str_replace("-wp", "", $ver);
	} else {
		$ver = '1.12.4';
	}

	wp_deregister_script('jquery');
	wp_register_script('jquery', "//ajax.googleapis.com/ajax/libs/jquery/$ver/jquery.js", false, $ver);
	wp_enqueue_script('jquery');
}

function script_that_requires_jquery() {
	wp_enqueue_style( 'style', get_stylesheet_uri() );
	wp_deregister_script('jquery-ui'); 
	wp_register_script('jquery-ui', ('https://ajax.googleapis.com/ajax/libs/jqueryui/1.13.0/jquery-ui.js'), false, '1.13.0'); 
	wp_enqueue_script('jquery-ui');  
}
